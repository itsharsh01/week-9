def split_into_blocks(text):
    blocks = text.split("\n\n")
    cleaned_blocks = []

    for block in blocks:
        block = block.strip()
        if block:
            cleaned_blocks.append(block)

    return cleaned_blocks


blocks = split_into_blocks(sample_text)

for i, b in enumerate(blocks):
    print(f"\nBlock {i+1}: {b}")

def classify_block(block):
    lower = block.lower()

    if "example" in lower:
        return "example"
    elif "question" in lower or "exercise" in lower:
        return "question"
    else:
        return "concept"


