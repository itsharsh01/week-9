def classify_block(text):
    t = text.lower().strip()

    if t.startswith(("1.", "2.", "3.", "q", "question")):
        return "question"

    if "example" in t or "illustration" in t:
        return "example"

    if "=" in t:
        return "formula"

    if len(t) < 60 and t.isupper():
        return "heading"

    return "concept"

def build_chunks_pipeline(raw_text):
    cleaned = clean_text(raw_text)

    blocks = split_into_blocks(cleaned)

    structured_blocks = []
    for b in blocks:
        structured_blocks.append({
            "text": b,
            "type": classify_block(b)
        })

    chunks = chunk_blocks(structured_blocks)

    return chunks

from transformers import AutoTokenizer

tokenizer = AutoTokenizer.from_pretrained("gpt2")

"""Hybrid Chunking (CORE PART 🔥)
🧠 Strategy
Preserve block meaning
Split only if too large
Add overlap
"""

def chunk_blocks(blocks, chunk_size=300, overlap=50):
    chunks = []

    for block in blocks:
        tokens = tokenizer.encode(block["text"])
        start = 0

        while start < len(tokens):
            end = start + chunk_size
            chunk_tokens = tokens[start:end]

            chunk_text = tokenizer.decode(chunk_tokens)

            chunks.append({
                "text": chunk_text,
                "type": block["type"]
            })

            start += chunk_size - overlap

    return chunks

